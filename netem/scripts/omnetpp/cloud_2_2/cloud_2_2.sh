	sudo tc qdisc add dev eth0 root handle 1: prio bands 3

	# apply delay to each band
	# -- blue1
	sudo tc qdisc add dev eth0 parent 1:2 handle 10: netem delay 321.42ms 37.89ms distribution cloud_2_2_p0_b1 loss 0.0329%
	# -- orange1
